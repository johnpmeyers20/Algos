function longestWord(text) {
  let arr = text.match(/[a-z]+/gi);
  console.log('arr', arr);
  let longest = '';
  for (let i = 0; i < arr.length; i++) {
      if (arr[i].length > longest.length) {
          longest = arr[i];
      }
  }
  console.log('longest', longest);
  return longest;
}